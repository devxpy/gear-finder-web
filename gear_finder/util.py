def compute(ratio, tol, gear_set):
    results = 0
    total_iterations = (
        len(gear_set) * (len(gear_set) - 1) * (len(gear_set) - 2) * (len(gear_set) - 3)
    )
    save_list = []
    for i in permutations(gear_set, 4):
        predicted_ratio = i[0] / i[1] * i[2] / i[3]
        compare = abs(predicted_ratio - ratio)
        if compare <= tol:
            config_result = (
                str(i[0]) + "/" + str(i[1]) + " x " + str(i[2]) + "/" + str(i[3])
            )
            save_list.append(
                [config_result, round(predicted_ratio, 10), round(compare, 10)]
            )
            results = results + 1
    return total_iterations, sorted(save_list, key=lambda x: x[2])



